public class HelloWorld {
    public static void main(String[] args) {
        // Print a welcome message
        System.out.println("Hello, Naresh! Welcome to Java.");

        // Example: calculate the sum of first 5 numbers
        int sum = 0;
        for (int i = 1; i <= 5; i++) {
            sum += i;
        }

        // Display the result
        System.out.println("The sum of numbers 1 to 5 is: " + sum);
    }
}
